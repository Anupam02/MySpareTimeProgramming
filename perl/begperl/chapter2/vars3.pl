print "Plus three is ", $b, "\n";
my $c = $b / 3;
print "All over three is " , $c, "\n";
my $d = $c + 1;
print "Add one is ", $d, "\n";
print "\nThose stages again: ", $a, " ", $b, " ", $c, " ", $d, "\n";

